// Casting: conversão explícita de tipo de dados

public class casting {

	public static void main(String[] args) {
		double a = 5.0;
		int b = (int) a * 2;

		System.out.println(b);

		int c, d;
		double resultado;
		c = 5;
		d = 2;
		resultado = (double) c / d;

		System.out.println(resultado);
		System.out.println();
	}

}
